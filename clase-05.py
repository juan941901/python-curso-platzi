"""Tipos de Datos en Python: Enteros, Flotantes y Booleanos"""
# Definición de variables

edad = 30 # Entero(int)
altura = 1.75 # Flotante(float)
es_estudiante = True # Booleano(bool)
exponencial = 1e3 # Notación científica, equivale a 1000.0
exponencial2 = 1e-3 # Notación científica, equivale a 0.001
# Imprime el tipo de las variables

print(type(edad))
print(type(altura))
print(type(es_estudiante))
print(type(exponencial))
print(type(exponencial2))

print(edad + 5) # Suma 5 a la variable edad
print(altura * 2) # Multiplica la variable altura por 2 
print(edad + altura) # Suma la variable edad y altura
